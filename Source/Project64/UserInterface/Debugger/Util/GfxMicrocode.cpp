uint32_t GfxMicrocode::Identify(uint8_t* ucode, ucode_info_t* info)
{
	info->commandTable = NULL;
	info->name = "Unknown microcode";
	info->version = UCODE_UNKNOWN;

	uint32_t checksum = 0;
	for (int i = 0; i < 3072; i += sizeof(uint32_t))
	{
		checksum += *(uint32_t*)&ucode[i];
	}

	for (int i = 0; Checksums[i].checksum != 0; i++)
	{
		if (Checksums[i].checksum == checksum)
		{
			info->version = Checksums[i].version;
			break;
		}
	}

	if (info->version == UCODE_UNKNOWN)
	{
		return checksum;
	}

	for (int i = 0; Microcodes[i].version != UCODE_UNKNOWN; i++)
	{
		if (Microcodes[i].version == info->version)
		{
			info->name = Microcodes[i].name;
			info->commandTable = Microcodes[i].commandTable;
			break;
		}
	}

	return checksum;
}

